    CREATE TABLE Certificates (
    NUMBER VARCHAR(50) NOT NULL,
    STATE VARCHAR(20) NOT NULL,
    CHECK (STATE IN ('en attente', 'acceptée', 'refusée')),
    CREATION_DATE DATE NOT NULL,
    CAR_SERIAL_NUMBER VARCHAR(50) NOT NULL, 
    PRIMARY KEY (NUMBER)
);

